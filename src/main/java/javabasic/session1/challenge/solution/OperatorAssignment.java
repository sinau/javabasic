package javabasic.session1.challenge.solution;

public class OperatorAssignment {
	public static void main(String[] args) {
		int a = 10;
		int b= 15;
		
		int c = 0;
		int d = 1;
		double e = 1;
		
		c += a;
		System.out.println(c);
		
		c++;
		System.out.println(c);
		
		d *= b;
		System.out.println(d);
		
		e /= (double) b;
		System.out.println(e);
	}
}
