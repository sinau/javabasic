package javabasic.session5.model;

public class Segitiga {
	private int alas;
	private int tinggi;
	
	public Segitiga(int alas, int tinggi) {
		this.alas = alas;
		this.tinggi = tinggi;
	}
	
	public int getAlas() {
		return alas;
	}
	public void setAlas(int alas) {
		this.alas = alas;
	}
	public int getTinggi() {
		return tinggi;
	}
	public void setTinggi(int tinggi) {
		this.tinggi = tinggi;
	}
}
